
public class SwitchCases {

	public static void main(String[] args) {
	 	
		int a=10;
		
		switch(a){
		case 10:
			System.out.println("case1");
			break;
		case  20:
			System.out.println("case2");
			break;
		case  30:
			System.out.println("case3");
			break;
		case  40:
			System.out.println("case4");
			
		}
	
	}
}
